#include "libvec.h"

t_vec		vec_rot_xyz(t_vec v, t_vec r)
{
	v = vec_rot_x(v, DTR(r.x));
	v = vec_rot_y(v, DTR(r.y));
	v = vec_rot_z(v, DTR(r.z));
	return (v);
}
