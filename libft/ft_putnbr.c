#include "libft.h"

void	ft_putnbr(int n)
{
	int		d;
	int		m;

	d = 1;
	m = 0;
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		n = 147483648;
	}
	if (n < 0)
	{
		n *= -1;
		ft_putchar('-');
	}
	while (n / d > 9)
		d *= 10;
	while (d >= 1)
	{
		ft_putchar('0' + ((n / d) - (m * 10)));
		m = n / d;
		d /= 10;
	}
}
