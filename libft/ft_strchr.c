#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != (char)c && s[i] != '\0')
		i++;
	return (s[i] != (char)c ? (char *)NULL : (char *)&s[i]);
}
