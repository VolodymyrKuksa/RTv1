#include "libft.h"

void	ft_clear_matrix(t_matrix **m)
{
	int		i;

	if (*m)
	{
		i = -1;
		while (++i < (*m)->i)
			free((*m)->arr[i]);
		free((*m)->arr);
		free(*m);
		*m = NULL;
	}
}
