#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		d;
	int		m;

	d = 1;
	m = 0;
	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		n = 147483648;
	}
	if (n < 0)
	{
		n *= -1;
		ft_putchar_fd('-', fd);
	}
	while (n / d > 9)
		d *= 10;
	while (d >= 1)
	{
		ft_putchar_fd(('0' + ((n / d) - (m * 10))), fd);
		m = n / d;
		d /= 10;
	}
}
